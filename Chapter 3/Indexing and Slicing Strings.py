mystring = "abcdefghijk"
print(mystring)

print(mystring[0]) # Indexing
print(mystring[-1]) # Indexing
print(mystring[0:5]) # Slicing
# You can also slice from one position to the end by:
print(mystring[6:])
print(mystring[:5]) # And vice versa
print(mystring[::2]) # Prints everything with a step of 2
print(mystring[::-1]) # Prints everything backwards!